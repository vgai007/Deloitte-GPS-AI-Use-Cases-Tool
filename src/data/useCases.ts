export const complexityToNumber = (complexity: string): number => {
  switch (complexity) {
    case "Low":
      return 1;
    case "Medium":
      return 2;
    case "High":
      return 3;
    default:
      return 2;
  }
};

export const impactToNumber = (impact: string): number => {
  switch (impact) {
    case "Low":
      return 1;
    case "Medium":
      return 2;
    case "High":
      return 3;
    case "Very High":
      return 4;
    default:
      return 2;
  }
};
